package practiceQuestions;

public class Multiply {

	public int mul(int a, int b) {
		
		int c =a*b;
		return c;
	}
	
   public float mul(float a, float b, float c) {
	
		return (a*b*c);
	}
   
   public double mul(double a, double b, double c, double d) {
	   
	   return(a*b*c*d);  
   }
   
   public long mul(long a, long b, long c, long d, long e) {
 	   
	   return(a*b*c*d*e);  
    }
   
   public int mul(int a, int b, int c, int d, int e, int f) {
		
		int s =a*b*c*d*e*f;
		return s;
	}
	
}
