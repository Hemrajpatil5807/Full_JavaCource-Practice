package practiceQuestions;

public class Addition {

	public int add(int a, int b) {
		
		int c =a+b;
		return c;
	}
	
   public float add(float a, float b, float c) {
	
		return (a+b+c);
	}
   
   public double add(double a, double b, double c, double d) {
	   
	   return(a+b+c+d);  
   }
   
   public long add(long a, long b, long c, long d, long e) {
 	   
	   return(a+b+c+d+e);  
    }
   
   public int add(int a, int b, int c, int d, int e, int f) {
		
		int s =a+b+c+d+e+f;
		return s;
	}
	
	
}
