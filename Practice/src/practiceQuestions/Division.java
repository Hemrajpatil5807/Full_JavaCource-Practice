package practiceQuestions;

public class Division {
	
	public int div(int a, int b) {
		
		int c =a/b;
		return c;
	}
	
   public float div(float a, float b, float c) {
	
		return (a/b/c);
	}
   
   public double div(double a, double b, double c, double d) {
	   
	   return(a/b/c/d);  
   }
   
   public long div(long a, long b, long c, long d, long e) {
 	   
	   return(a/b/c/d/e);  
    }
   
   public int div(int a, int b, int c, int d, int e, int f) {
		
		int s =a/b/c/d/e/f;
		return s;
	}
	

}
